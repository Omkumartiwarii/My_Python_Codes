#Arithmatic operators: +,-,*,/,%
A=10
B=5
print(A+B)
print(A-B)
print(A*B)
print(A/B)
print(A%B)

#Assignment Operators: =,+=,-=,*=,/=
A=5-4
print(A)
A+=5
print(A)

#comparison Operators: ==,!=,>,<,>=,<=
print(A==B)
print(A!=B)
print(A>B)
print(A<B)
print(A>=B)
print(A<=B)

#Logical Operators: and, or, not
print(A<B and A!=B)
print(A<B or A!=B)
print(not(A<B))

print(True and False)
print(True or False)
print(not True)
